        protected override int ExecutePart1()
        {
            var visits = new HashSet<(int x, int y)>()
            {
                (0, 0)
            };

            int x = 0;
            int y = 0;
            for (int i = 0; i < direction.Length; i++)
            {
                switch (direction[i])
                {
                    case '>':
                        x++;
                        break;
                    case '<':
                        x--;
                        break;
                    case '^':
                        y++;
                        break;
                    case 'v':
                        y--;
                        break;
                    default:
                        break;
                }
                
                if (!visits.Contains((x, y)))
                {
                    visits.Add((x, y));
                }
            }

            return visits.Count;
        }

        protected override int ExecutePart2()
        {
            var visits = new HashSet<(int x, int y)>()
            {
                (0, 0)
            };

            int[] x = { 0, 0 };
            int[] y = { 0, 0 };
            for (int i = 0; i < direction.Length; i++)
            {
                int index = i % 2;
                switch (direction[i])
                {
                    case '>':
                        x[index]++;
                        break;
                    case '<':
                        x[index]--;
                        break;
                    case '^':
                        y[index]++;
                        break;
                    case 'v':
                        y[index]--;
                        break;
                    default:
                        break;
                }
                
                if (!visits.Contains((x[index], y[index])))
                {
                    visits.Add((x[index], y[index]));
                }
            }

            return visits.Count;
        }

